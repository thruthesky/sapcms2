			<img src='/theme/wooreeedu/img/recommended_2.png'>
			<div class='text'>
				<span class='label copper'>International School Manila</span>는 그 규모와 시설, 커리큘럼 등 모든 면에서 자타가 인정하는 필리핀의 고급 국제 학교입니다. 필리핀에 거주하는 미국인과 영국인을 위해 1920년에 세워졌으며 대부분 대사관 및 주재원의 자녀들이 학생으로 구성되어 있고 입학이 까다로운 편입니다.
			</div>
			<div class='text'>
				ISM의 졸업생들의 대부분이 미국이나 영국의 대학으로 입학하며 ISM의 규모 또한 International School 캠퍼스 중 큰 면적을 자랑합니다.
			</div>
			<div class='text'>
				ISM의 경우 한국인에게 할당된 T.O는 전체 학생 수의 20%정도인데 거의가 2~3년을 기다려야 입학시험을 볼 수 있을 정도로 시간이 걸립니다. 그러므로 ISM에 입학을 희망한다면 일단 서류를 접수하고 필리핀내의 다른 학교를 다니면서 T.O를 기다렸다가 입학하는 것이 좋습니다.
			</div>
			<div class='text'>
				<span class='label black'>또한</span> <span class='label red'>ISM은 부모가 워킹비자(9G) 이상이어야 입학이 가능합니다.</span>
