			<div class='star-label'>★ Southville International School</div>
			<img src='/theme/wooreeedu/img/recommended_3.png'>
			<div class='text'>
				국제학교로 ESL과정이 있어서 한국인 학생들이 쉽게 다닐 수 있는 학교입니다. 초등학생에게는 적극 추천할 수 있는 학교입니다.<br>
				정규반으로 입학할 경우 영어로 수업이 이루어지며, 필리핀 명문 사립의 단점인 따갈로그 수업이 없고 다국적 아이들과의 교류가 가능합니다. 또한 다니는 아이들의 수준도 상당히 높은 편이고 고학년의 경우 ESL반의 부작용이 있지만 저학년은 아직 좋은 환경을 유지하고 있습니다.<br>
				1990년 10월에 설립된 사우스빌 인터내셔널 스쿨은 알라방에서 인근의 빠라냐케시티의 BF HOMES라는 곳에 위치하는 초등부터 대학까지 있는 명문사립학교입니다. 일반 서민에게는 부담이 되는 브렌트와는 달리 학비가 1년에 약 400~500만원 내외로 한국인들에게 가장 인기 있는 학교이며, 최근 많은 학생들이 몰리고 있기에 입학 (정시입학 6월, IB는 8월)을 미리 미리 서둘러야 합니다.
			</div>
			<div class='text'>
				•한국인 학생 비율은 전체 35% (Main 15%, ESL 85%), 학년에 따라 차이가 있습니다.<br>
				•일반적으로 고교는 11학년제로 졸업, IB는 1년을 더 다녀 대학 진학. (총 12학년제)<br>
				•기부금 1,500달러, ILC (ESL) 추가비용 46,750페소, 교복 (상하/대) 990페소, 체육복 (상하/대) 615페소, 스쿨버스 3,100페소 등으로 총 134,069페소 + 1,500달러 입니다.
