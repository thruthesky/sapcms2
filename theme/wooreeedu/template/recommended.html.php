			<img src='/theme/wooreeedu/img/recommended_1.png'>
			<div class='text'>
				<span class='label copper'>100년 역사의 필리핀 최고 명문국제학교</span>
			</div>
			<div class='text'>
				100년 역사의 필리핀 최고 명문국제학교인 브렌트 국제학교는 약 100년 전인 1909년에 캐나다인 Charles Henry Brent 목사에 의해 현재의 브렌트스쿨 바기오 캠퍼스 자리에서 당시 바기오 지역에 많이 거주하고 있던 미국인 자녀들을 위한 기숙식 학교로 설립하였으며, 1984년도에 라구나에 또 하나의 브렌트 국제학교인 브렌트스쿨 (Brent International School Manila)을 설립하였으며 훌륭한 학교시설과 수준 높은 교육방침을 가진 필리핀에서 손꼽히는 인터내셔널 스쿨로 성장하였습니다.
			</div>
			<div class='text'>
				브렌트스쿨은 단지 학과 위주의 수업만이 아닌 많은 교내 외의 재즈댄스, 악기연주, 태권도, 수영, 골프, 축구클럽등과 같은 클럽활동을 즐길 수 있습니다
			</div>
			<div class='text'>
				<span class='label black'>국제학위 (IB) 공인학교</span>
			</div>
			<div class='text'>
				BIS는 필리핀의 학제와는 다르게 초등학교 1학년부터 고등학교 3학년에 해당하는 12학년제로 운영이 되고 있지만 특히 아시아권 학교들 중에서는 극소수에 지나지 않는 “국제학위 (International Baccalaureate)” 공인   학교이기 때문에 11학년과 12학년 과정을 이수하고 스위스 제네바에 있는 IB본부로부터 IB자격을 인정받으면, 필리핀 내 다른 대학은 물론 서구권 유명대학에 우선 입학의 혜택을 받을 수가 있습니다.
			</div>
			<div class='text'>
				브렌트스쿨은 아래의 기관들에 의하여 공인되어 있습니다.
